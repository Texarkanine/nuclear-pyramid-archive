But is the proposition true? There is a test for establishing or rejecting the proposition. The vacuum chamber with the magnet inside would have to be assembled. With adequate temperature, atomic hydrogen would be injected into the vacuum chamber to pass between the poles of the magnet. If the hydrogen stream splits into two streams, one to each magnetic pole, then it would be established that the apparatus works correctly. One would now, in a second trial, inject atomic deuterium into the vacuum. If the deuterium behaves like the hydrogen, then the proposition of this paper is false. If the behavior of the deuterium is distinctly different, then there is evidence for further consideration of the proposition of this paper.<br><br>

